  // Sorted longest-dial-code-first, used to match an existing E.164 number
  // back to a country (so "+251911..." resolves to Ethiopia, not "+2" -> Egypt).
  export const COUNTRY_CODES_BY_DIAL_LENGTH = [...COUNTRY_CODES].sort(
    (a, b) => b.dial.length - a.dial.length
  )
  
  export function parsePhone(fullPhone) {
    if (!fullPhone || !fullPhone.startsWith('+')) {
      return { iso2: 'ET', number: '' } // default Ethiopia
    }
    const digits = fullPhone.slice(1)
    const match = COUNTRY_CODES_BY_DIAL_LENGTH.find(c => digits.startsWith(c.dial))
    if (!match) return { iso2: 'ET', number: digits }
    return { iso2: match.iso2, number: digits.slice(match.dial.length) }
  }
  
  export function getCountry(iso2) {
    return COUNTRY_CODES.find(c => c.iso2 === iso2) || COUNTRY_CODES.find(c => c.iso2 === 'ET')
  }